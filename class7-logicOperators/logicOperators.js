// Declaramos una variable para probar las condiciones
let num = 0;

// Condicional if: verifica si el número es positivo
if (num > 0) {
    console.log("El número es positivo");
} 
// Condicional else if: verifica si el número es negativo
else if (num < 0) {
    console.log("El número es negativo");
} 
// Condicional else: se ejecuta si ninguna de las condiciones anteriores es verdadera
else {
    console.log("El número es cero");
}